/**
 * Dieses Interface beinhaltet die onClick-Methode
 * welches jedes Element der Benutzeroberflaeche aufweisen muss.
 * 
 * @author Felix Hildebrandt
 *
 */
public interface ClickListener {
	
	/**
	 * Ereignismethode beim Klicken auf das
	 * Element der Benutzeroberflaeche.
	 */
	public void onClick();
}
